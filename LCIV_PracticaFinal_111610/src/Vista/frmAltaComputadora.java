    private void cargarCombos() {

        ArrayList<Aula> aulas = gestor.getAulas();
        ArrayList<Estado> estados = gestor.getEstados();
        DefaultComboBoxModel modelAula = new DefaultComboBoxModel();
        DefaultComboBoxModel modelEstado = new DefaultComboBoxModel();
        for (Aula aula : aulas) {
            modelAula.addElement(aula);
        }
        for (Estado est : estados) {
            modelEstado.addElement(est);
        }
        cboAulas.setModel(modelAula);
        cboEstado.setModel(modelEstado);

    }
