    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        int funcionamiento = (Integer) spnFuncionamiento.getValue();

        if (funcionamiento >= 0 && funcionamiento <= 100) {
            Aula aula = (Aula) cboAulas.getSelectedItem();
            Estado estado = (Estado) cboEstado.getSelectedItem();
            
            GestorBD gestor = new GestorBD();
            Computadora nueva = new Computadora(0, aula, estado, funcionamiento);
            gestor.agregarComputadora(nueva);
            JOptionPane.showMessageDialog(null, nueva.toString(), "Computadora Registrada",1);
            int resultado = JOptionPane.showConfirmDialog (null, "Desea cargar una nueva Computadora?","Info",JOptionPane.YES_NO_OPTION);
            if (resultado == JOptionPane.YES_OPTION) {                
                cboAulas.setSelectedIndex(0);
                cboEstado.setSelectedIndex(0);
                spnFuncionamiento.setValue(0);
            }
            else
            {
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un valor válido", "Error", JOptionPane.ERROR_MESSAGE);
            spnFuncionamiento.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

