    private void cargarTabla() {
        ArrayList<DTOOfertas> lista = gestor.listadoOfertas();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[] {"Producto","Pr.Normal",
            "Pr.Oferta","Stock","Fecha Inicio","DÃ­as Vigencia"});
        for (DTOOfertas item : lista) {
            modelo.addRow(new Object[] {item.getNombre(),item.getPrecioNormal(),
                item.getPrecioOferta(),item.getStockDisponible(),
                item.getFechaInicio(),item.getDiasVigencia()});
        }
        tblListado.setModel(modelo);
    }
